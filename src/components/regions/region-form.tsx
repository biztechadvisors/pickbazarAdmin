export default function CreateOrUpdateRegionsForm({ initialValues }: IProps) {
  const router = useRouter();
  const { t } = useTranslation();
  const { data: me } = useMeQuery();
  const {
    register,
    handleSubmit,
    formState: { errors },
    setValue,
    watch,
  } = useForm<Region>({
    shouldUnregister: true,
    resolver: yupResolver(RegionsValidationSchema),
    defaultValues: initialValues ?? defaultValues,
  });

  const shop_id = me?.shop_id ;


  const { mutate: createTaxClass, isLoading: creating } =
    useCreateRegionsClassMutation(shop_id);
  const { mutate: updateRegionClass, isLoading: updating } =
    useUpdateRegionClassMutation(shop_id);

  const onSubmit = async (values: Region) => {
    console.log('values = ',values)
    if (initialValues) {
      updateRegionClass({
        id: initialValues.id!,
        ...values,
      });
    } else {
    console.log('values = ',values)
      createTaxClass({
        ...values,
      });
    }
  };

  const [selectedCountry, setSelectedCountry] = useState<string>('');

  const handleCountryChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
    setSelectedCountry(e.target.value);
  };

  return (
    <form onSubmit={handleSubmit(onSubmit)}>
      <div className="my-5 flex flex-wrap sm:my-8">
        <Description
          title={t('form:form-title-regions')}
          details={`${initialValues
            ? t('form:item-description-update')
            : t('form:item-description-add')
            } ${t('form:tax-form-info-help-text')}`}
          className="w-full px-0 pb-5 sm:w-4/12 sm:py-8 sm:pe-4 md:w-1/3 md:pe-5 "
        />

        <Card className="w-full sm:w-8/12 md:w-2/3">
         
        <div className="mb-5">
          <label className="block text-sm font-medium text-gray-700">
            {t('form:input-label-name')}
          </label>
          <select
           {...register('name')}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-indigo-500 focus:ring-indigo-500 sm:text-sm"
            value={ selectedCountry?selectedCountry: countryData.some((country) => country.code === initialValues?.name)
              ? initialValues?.name
              : selectedCountry}
            onChange={handleCountryChange}>
            <option value="" disabled>
              {t('')}
            </option>
            {countryData.map(country => (
              <option key={country.code} value={country.code}>
                {country.name}
              </option>
            ))}
            </select>
            {errors.name && <p className="text-red-500 text-sm">{errors.name.message}</p>}
          </div>
        </Card>
      </div>

      <div className="mb-4 text-end">
        <Button
          variant="outline"
          onClick={router.back}
          className="me-4"
          type="button"
        >
          {t('form:button-label-back')}
        </Button>
        <Button loading={creating || updating}>
          {initialValues
            ? t('form:button-label-update')
            : t('form:button-label-add')}{' '}
          {t('form:form-title-regions')}
        </Button>
      </div>
    </form>
  );
}